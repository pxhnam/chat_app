    private void init() {
        body.setLayout(new MigLayout("fillx", "", "5[bottom]5"));
        sp.setVerticalScrollBar(new ScrollBar());
        sp.getVerticalScrollBar().setBackground((Color.WHITE));
    }

    public void addLeft(String text, String time, String user) {
        Chat_Left_With_Profile left = new Chat_Left_With_Profile();
        left.setText(text, time);
        left.setUserProfile(user);
        body.add(left, "wrap, w 100::80%");
        body.repaint();
        body.revalidate();
        scrollToBottom();
    }

    public void addRight(String text, String time) {
        Chat_Right right = new Chat_Right();
        right.setText(text, time);
        body.add(right, "wrap, al right, w 100::80%");
        body.repaint();
        body.revalidate();
        scrollToBottom();
    }

    public void addDate(String date) {
        Chat_Date item = new Chat_Date();
        item.setDate(date);
        body.add(item, "wrap, al center");
        body.repaint();
        body.revalidate();
    }

    public void clearChat() {
        body.removeAll();
        repaint();
        revalidate();
    }

    private void scrollToBottom() {
        JScrollBar verticalBar = sp.getVerticalScrollBar();
        AdjustmentListener downScroller = new AdjustmentListener() {
            @Override
            public void adjustmentValueChanged(AdjustmentEvent e) {
                Adjustable adjustable = e.getAdjustable();
                adjustable.setValue(adjustable.getMaximum());
                verticalBar.removeAdjustmentListener(this);
            }
        };
        verticalBar.addAdjustmentListener(downScroller);
    }

